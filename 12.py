# Assignment Operators

a = 10
b = 20
a += b
print("a=", a, "b=", b)  # a = a + b

a -= b
print("a=", a, "b=", b)  # a = a - b 

a *= b
print("a=", a, "b=", b)  # a = a * b

a /= b
print("a=", a, "b=", b)  # a = a / b

a //= b
print("a=", a, "b=", b)  # a = a // b

a %= b
print("a=", a, "b=", b)  # a = a % b