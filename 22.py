#String Methods

s = " Aptech Learning "
print("Original string:", s)
s1 = s.upper()
print("Uppercase string:", s1)  
s2 = s.lower()
print("Lowercase string:", s2)
s3 = s.capitalize()
print("Capitalized string:", s3)
s4 = s.replace("A", "B")
print("Replaced string:", s4)
s5 = s.split()
print("Split string:", s5)
print("Length of string:", len(s))
print("Removing whitespaces :", s.strip())